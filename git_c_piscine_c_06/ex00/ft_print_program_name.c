#include <unistd.h>

int	main(int argc, const char *argv[])
{
	if (argc != 0)
	{
		while (*argv[0] != '\0')
		{
			write(1, argv[0], 1);
			(argv[0])++;
		}
	}
	write(1, "\n", 1);
}
